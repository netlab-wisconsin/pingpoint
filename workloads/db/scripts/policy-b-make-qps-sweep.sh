#!/bin/bash

P_ACTIVE_XCD=2
P_TARGET_CC=0
P_WORKERS=4

BE_WORKERS_PER_XCD=32 # 8 doesn't affect P. Sweep 16 or 32. 32 affects P.
BE_BATCH_CHUNKS=64

for P_ARRIVAL_QPS in 800000 1000000 1200000 1400000 1600000 1800000 2000000; do
    make -B policy-b \
      P_ACTIVE_XCD="${P_ACTIVE_XCD}" \
      P_TARGET_CC="${P_TARGET_CC}" \
      P_WORKERS="${P_WORKERS}" \
      P_ARRIVAL_QPS="${P_ARRIVAL_QPS}" \
      BE_WORKERS_PER_XCD="${BE_WORKERS_PER_XCD}" \
      BE_BATCH_CHUNKS="${BE_BATCH_CHUNKS}"
done 